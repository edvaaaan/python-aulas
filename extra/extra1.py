# STRINGS EM PYTHON

word = 'Edvan'
print(len(word)) # -> TAMANHO DA PALAVRA
print(word.find('a')) # -> DIZ EM QUE POSIÇÃO ESTÁ A LETRA
print(word.capitalize()) # -> PRIMEIRA LETRA EM MAIUSCULA
print(word.upper()) # -> DEIXA EM CAIXA ALTA
print(word.lower()) # -> DEIXA EM CAIXA BAIXA
print(word.isalpha()) # -> VE SE É ALFABETICA
print(word.isdigit()) # -> VE SE É DIGITO
print(word.count('d')) # -> CONTA O NUMERO DE LETRAS
print(word.replace('a', 'x')) # -> FAZ UMA TROCA DE LETRAS
print(word*3) # -> ESCREVE O NOME X VEZES 